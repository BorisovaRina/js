console.log("5" + 2);      // "52" — неявное: число 2 → строка, конкатенация 
console.log("5" - 2);      // 3 — неявное: строка "5" → число, вычитание 
console.log(Boolean("0")); // true — "0" truthy (непустая строка) 
console.log(Boolean(""));  // false — пустая строка falsy 

/*
Этот код демонстрирует неявные преобразования: "+" всегда приводит к строке, "-" — к числу; Boolean() явно проверяет truthy/falsy значения (0, "", null/false — falsy, остальное truthy).
*/